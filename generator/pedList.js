export async function generatePeds() {
    let data = await getDataFromGithub(githubUrl.peds);
    if (data) {
        data = data.filter(x => !x.DlcName.toLowerCase().includes('g9ec'));
        let result = '<?xml version="1.0" encoding="ISO-8859-1"?>\n' +
            '<PedList>\n';
        for(const mClass of Object.keys(menyooClasses)) {
            result += `\t<Category name=\"${mClass}\">\n`;
            result += getClassPeds(data, mClass);
            result += `\t</Category>\n`;
        }

        result += `</PedList>`;
        writeToFile(fileNames.pedList, result);
    }
}

function getClassPeds(data, mClass) {
    const classPeds = data.filter(x => x.Name.toLowerCase().startsWith(menyooClasses[mClass]));
    let peds = '';
    for(const pedData of classPeds) {
        const ped = `\t\t<Ped name=\"${pedData.Name.toLowerCase()}\" caption=\"${getPedName(pedData)}\"/>\n`;
        peds += ped;
    }
    if (mClass === 'Player') {
        peds += `\t\t<Ped name=\"mp_f_freemode_01\" caption=\"Freemode Female\"/>\n`;
        peds += `\t\t<Ped name=\"mp_m_freemode_01\" caption=\"Freemode Male\"/>\n`;
    }
    if (mClass === 'Story') {
        peds += `\t\t<Ped name=\"player_one\" caption=\"Franklin\"/>\n`;
        peds += `\t\t<Ped name=\"player_two\" caption=\"Trevor\"/>\n`;
        peds += `\t\t<Ped name=\"player_zero\" caption=\"Michael\"/>\n`;
    }
    return peds;
}

function getPedName(pedData){
    return pedData.TranslatedDirectorName?.English || getPedFallbackName(pedData);
}

function getPedFallbackName(pedData){
    return pedFallbackNames[pedData.Name] || pedData.Name;
}